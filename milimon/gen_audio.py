texts = set()
for n in LETTER_NAMES:
    texts.add("אֵיפֹה הָאוֹת " + n)
    texts.add("זֹאת הָאוֹת " + n)
texts.update(SYLLS); texts.update(BUILD_SYLLS); texts.update(WORDS)
texts.update(MONSTERS); texts.update(PRAISES); texts.update(PHRASES)
texts.update(SENTENCES); texts.update(STORY_LINES); texts.update(STORY_QS)
texts.update(LONG_STORY_LINES); texts.update(LONG_STORY_QS)
for s in SENTENCES:                    # word chips for sentence-building
    texts.update(s.split(" "))

def norm(t):  # must match the JS norm() in the game
    return re.sub(r"\s+", " ", re.sub(r'[?!.,:;"״]', " ", t)).strip()

jobs = {}  # key -> (speak_text, filename)
for t in sorted(texts):
    k = norm(t)
    fn = hashlib.md5(k.encode("utf-8")).hexdigest()[:10] + ".mp3"
    jobs[k] = (t, fn)

sem = asyncio.Semaphore(6)
done = [0]
async def gen(k, speak_text, fn):
    path = os.path.join(OUT, fn)
    if os.path.exists(path) and os.path.getsize(path) > 1000:
        done[0] += 1; return True
    async with sem:
        for attempt in range(3):
            try:
                await edge_tts.Communicate(speak_text, VOICE, rate=RATE).save(path)
                if os.path.getsize(path) > 500:
                    done[0] += 1
                    if done[0] % 20 == 0: print(f"{done[0]}/{len(jobs)}", flush=True)
                    return True
            except Exception as e:
                await asyncio.sleep(1.5 * (attempt + 1))
        print("FAILED: " + k, flush=True)
        return False

async def main():
    results = await asyncio.gather(*[gen(k, t, fn) for k, (t, fn) in jobs.items()])
    ok = sum(1 for r in results if r)
    manifest = {k: fn for k, (t, fn) in jobs.items()
                if os.path.exists(os.path.join(OUT, fn)) and os.path.getsize(os.path.join(OUT, fn)) > 500}
    with open(os.path.join(OUT, "manifest.js"), "w", encoding="utf-8") as f:
        f.write("window.AUDIO_MANIFEST=" + json.dumps(manifest, ensure_ascii=False) + ";")
    print(f"DONE {ok}/{len(jobs)} clips, manifest has {len(manifest)} entries")

asyncio.run(main())
